#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int	i;

	i = 0;
	if (!s1 | !s2 | n < 0)
		return (-1);
	while (s1[i] != '\0' && i < n)
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	if (s2[i] != '\0' && i < n)
		return ((unsigned char)s1[i] - (unsigned char)s2[i]);
	return (0);
}
