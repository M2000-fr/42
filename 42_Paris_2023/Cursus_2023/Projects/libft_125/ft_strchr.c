#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int	i;

	i = -1;
	while (s[++i])
		if (s[i] == c % 256)
			return (s + i);
	if (c == '\0' && s[i] == '\0')
		return (s + i);
	return (0);
}
