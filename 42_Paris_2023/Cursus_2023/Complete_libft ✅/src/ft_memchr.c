#include "libft.h"

void	*ft_memchr(void *s, int c, size_t n)
{
	char	*str;
	size_t	i;

	str = (char *)s;
	i = 0;
	while (i < n)
	{
		if ((char)c == str[0])
			return ((void *)str);
		str++;
		i++;
	}
	return (NULL);
}
