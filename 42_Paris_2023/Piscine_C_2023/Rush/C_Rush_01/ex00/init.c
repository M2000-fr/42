#include "ft_main.h"

void	init_tab(int (*tab)[4][4])
{
	int	y;
	int	x;

	y = 0;
	while (y < 4)
	{
		x = 0;
		while (x < 4)
		{
			(*tab)[y][x] = 0;
			x++;
		}
		y++;
	}
}
