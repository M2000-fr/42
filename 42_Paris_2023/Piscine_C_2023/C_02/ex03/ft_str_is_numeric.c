int	ft_str_is_numeric(char *str)
{
	int	add;

	add = 0;
	while (str[add] != '\0' && ((str[add] >= '0' && str[add] <= '9')))
		add++;
	if (str[add] == '\0')
		return (1);
	else
		return (0);
}
