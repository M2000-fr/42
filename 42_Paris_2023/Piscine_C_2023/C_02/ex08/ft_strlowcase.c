char	*ft_strlowcase(char *str)
{
	int	add;

	add = 0;
	while (str[add] != '\0')
	{
		if (str[add] >= 'A' && str[add] <= 'Z')
			str[add] += 32;
		add++;
	}
	return (str);
}
