#include "libft.h"

int		ft_nbpcs(const char *f)
{
	int		a;
	int		count;

	a = 0;
	count = 0;
	while (f[a] != '\0')
	{
		if (f[a] == '\n' && f[a - 1] == '\n' && f[a] != '\0')
			count++;
		a++;
	}
	return (count + 1);
}
