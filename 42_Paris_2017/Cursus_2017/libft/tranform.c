#include "libft.h"

char	**ft_transform(char **tab)
{
	int		a;
	int		b;
	int		c;

	a = 65;
	b = 0;
	while(tab[b] != 0)
	{
		c = 0;
		while (tab[b][c] != '\0')
		{
			if (tab[b][c] == '#')
				tab[b][c] = a;
			c++;
		}
		b++;
		a++;
	}
	return (tab);
}
